    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int mabd = Integer.parseInt(cbMabd.getSelectedItem().toString());
        int masach = Integer.parseInt(cbMas.getSelectedItem().toString());
        if(trung2Ma(mabd, masach)){
            JOptionPane.showMessageDialog(this, "Trung 2 ma");
            cbMabd.requestFocus();
            return;
        }
        int sl = 0;
        int sltv  = getSachByMa(masach).getSoluong();
        try{
            sl = Integer.parseInt(jTextField1.getText());
            if(sl>sltv) {
                JOptionPane.showMessageDialog(this, "Khong du sach");
                jTextField1.requestFocus();
                return;
            }
            
            String ttrang = jTextField2.getText();
            QLMuon ql = new QLMuon(getBanDocByMa(mabd), getSachByMa(masach), sl, ttrang);
            tm.addRow(ql.toObject());
            lqlMuon.add(ql);
            getSachByMa(masach).setSoluong(sltv - sl);
            IOFile.viet(fsach, lsach);
            
        }catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Nhap so");
            jTextField1.requestFocus();
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

