    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        List<QLMuon> lsearch = new ArrayList<>();
        String key = jTextField3.getText().trim().toLowerCase();
        for(QLMuon i:lqlMuon)
        {
            if((i.getBandoc().getHoten()).toLowerCase()
                    .contains(key)|| (i.getSach().getTen().toLowerCase().contains(key)))
            {
                lsearch.add(i);
            }
        }
        tm.setRowCount(0);
        for(QLMuon i : lsearch)
        {
            tm.addRow(i.toObject());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

